# --- Helpers for outputting in a single JSON array, appending each entry ---

def append_result(entry):
    """
    Load the existing JSON array, append one entry, and rewrite the file.
    Creates the file with [entry] if it doesn't exist.
    """
    if os.path.exists(OUTPUT_PATH):
        with open(OUTPUT_PATH, "r+", encoding="utf-8") as f:
            try:
                data = json.load(f)
            except json.JSONDecodeError:
                data = []
            data.append(entry)
            f.seek(0)
            json.dump(data, f, ensure_ascii=False, indent=2)
            f.truncate()
    else:
        with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
            json.dump([entry], f, ensure_ascii=False, indent=2)

# --- Your existing helper functions remain unchanged ---

def is_english(text):
    try:
        return detect(text) == "en"
    except:
        return False

def is_excluded(url):
    return any(domain in url for domain in EXCLUDE_DOMAINS)

def extract_blog_content(url):
    try:
        resp = requests.get(url, headers=HEADERS, timeout=10)
        if resp.status_code != 200:
            print(f"â†’ {url} returned {resp.status_code}")
            return None, None

        soup = BeautifulSoup(resp.text, 'html.parser')
        text = " ".join(soup.stripped_strings)
        print(f"â†’ Fetched {len(text)} chars from {url}")

        tokens = nltk.word_tokenize(text)
        print(f"â†’ Token count: {len(tokens)}")

        if not is_english(text):
            print("   âœ— non-English")
            return None, None
        if len(tokens) < 300:
            print("   âœ— too short")
            return None, None

        title = soup.title.string.strip() if soup.title else ""
        return text, title

    except Exception as e:
        print(f"â†’ exception for {url}: {e!r}")
        return None, None


def google_search(query, num_pages=5):
    all_links = []
    for i in range(num_pages):
        paginated_query = f"{query} blog {i}"
        urls = google_search_serper(paginated_query)
        urls = [u for u in urls if not is_excluded(u)]
        all_links.extend(urls)
        time.sleep(1)
    return all_links

# --- Main scraping function, unchanged logic, only calls append_result() ---

def scrape_queries(queries, num_pages=5):
    total_saved = 0

    for query in queries:
        print(f"\nðŸ” Query: {query}")
        urls = google_search(query, num_pages)
        rank = 1
        saved_count = 0

        for url in tqdm(urls,
                        desc=f"ðŸ”„ Processing blogs for: {query[:40]}",
                        ncols=100):
            url_hash = hashlib.md5(url.encode()).hexdigest()
            if url_hash in visited_urls:
                continue
            visited_urls.add(url_hash)

            text, title = extract_blog_content(url)
            if text:
                result = {
                    "query": query,
                    "title": title,
                    "url": url,
                    "content": text,
                    "rank": rank  
                }
                append_result(result)   # <-- Appends into a single JSON array file
                print(f"âœ… Saved: {title[:60]}...")
                rank += 1
                saved_count += 1
                total_saved += 1
            else:
                print(f"âŒ Skipped: {url[:60]}...")

            time.sleep(1)

        print(f"ðŸ“¦ Blogs saved for this query: {saved_count}")
        print(f"ðŸ“Š Total blogs scraped so far: {total_saved}")


